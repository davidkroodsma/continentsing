				<h2>About the book</h2>
				
				<h5>Join birdsong expert Donald Kroodsma on a ten-week, ten-state bicycle journey as he travels with his son from the Atlantic to the Pacific, lingering and listening to our continent sing as no one has before. </h5>
				<p>On remote country roads over terrain vast and spectacular, from dawn to dusk and sometimes through the night, you will gain a deep appreciation for the natural symphony of birdsong many of us take for granted. Come along and marvel at how expressive these creatures are as Kroodsma leads you west across nearly five thousand miles--at a leisurely pace that enables a deep listen.</p>
				<p>Listening to a Continent Sing is also a guided tour through the history of a young nation and the geology of an ancient landscape, and an invitation to set aside the bustle of everyday life to follow one's dreams. It is a celebration of flowers and trees, rocks and rivers, mountains and prairies, clouds and sky, headwinds and calm, and of local voices and the people you will meet along the way. It is also the story of a father and son deepening their bond as they travel the slow road together from coast to coast.</p>
				<p>Beautifully illustrated throughout with drawings of birds and scenes and featuring QR codes that link to audio birdsong, this poignant and insightful book takes you on a travel adventure unlike any other--accompanied on every leg of your journey by birdsong.</p>
				
				<h3>About the author</h3> 
				<p>Donald Kroodsma is professor emeritus of ornithology at the University of Massachusetts, Amherst, and a world-renowned authority on birdsong. He is the author of The Singing Life of Birds, The Backyard Birdsong Guides, and Birdsong by the Seasons. He lives in Hatfield, Massachusetts. For more information, see DonaldKroodsma.com. </p>
				
